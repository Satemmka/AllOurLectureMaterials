package Lecture2;

public class Task4Calculator {


    public void sum (int a, int b){
        int sum = a + b;
        System.out.println("number one " + a + ", number two " + b + ", sum = " + sum);
    }

    public void subst (int a, int b) {
        int subst = a - b;
        System.out.println("number one " + a + ", number two " + b + ", sum = " + subst);
    }

    public void multi (int a, int b) {
        int multi = a * b;
        System.out.println("number one " + a + ", number two " + b + ", sum = " + multi);
    }

    public void division(int a, int b) {
        int division = a / b;
        System.out.println("number one " + a + ", number two " + b + ", sum = " + division);
    }


}
